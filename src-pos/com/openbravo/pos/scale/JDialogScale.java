    public static Double showEditNumber(Component parent, String title, String message, Icon icon, ProductInfoExt product) {
        
        Window window = SwingUtilities.windowForComponent(parent);
        
        // JNumberDialog myMsg;
        if (window instanceof Frame) { 
            myMsg = new JDialogScale((Frame) window, true);
        } else {
            myMsg = new JDialogScale((Dialog) window, true);
        }
        
        myMsg.m_product = product;
       
        myMsg.jLabelNameValue.setText(product.getName());
        myMsg.setTitle(title, message, icon);
        myMsg.setVisible(true);
        
        return myMsg.m_value;
    }
    
