    private void updatePrice(Double number) {
        if (number != null) {
            //Formats.CURRENCY.formatValue(number * m_product.getPriceSell());
            m_jPrice.setText(Formats.CURRENCY.formatValue(number * m_product.getPriceSell()));
        } else {
            m_jPrice.setText("0");
        }
    }
    
    // 01-02-2014 De Kruidenier
    private void updateNumber(Double number) {
        m_jnumber.setDoubleValue(number);
        
        m_value = m_jnumber.getDoubleValue();

        // Update price
        updatePrice(m_value);
    }
    
